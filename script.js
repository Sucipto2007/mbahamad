// ===== HERO SLIDER =====
let currentHeroSlide = 0;
const totalHeroSlides = 4;
let heroSliderTouchStart = 0;
let heroSliderTouchEnd = 0;
let heroAutoPlayInterval;

function initHeroSlider() {
  const sliderElement = document.getElementById('heroSlider');
  
  // Touch events
  sliderElement.addEventListener('touchstart', (e) => {
    heroSliderTouchStart = e.changedTouches[0].screenX;
  }, false);

  sliderElement.addEventListener('touchend', (e) => {
    heroSliderTouchEnd = e.changedTouches[0].screenX;
    handleHeroSwipe();
  }, false);

  // Mouse drag events
  let isDragging = false;
  let startX = 0;

  sliderElement.addEventListener('mousedown', (e) => {
    isDragging = true;
    startX = e.clientX;
    clearInterval(heroAutoPlayInterval);
  });

  sliderElement.addEventListener('mousemove', (e) => {
    if (!isDragging) return;
  });

  sliderElement.addEventListener('mouseup', (e) => {
    if (!isDragging) return;
    isDragging = false;
    const endX = e.clientX;
    const diff = startX - endX;
    
    if (Math.abs(diff) > 50) {
      if (diff > 0) slideHero(1);
      else slideHero(-1);
    }
    startAutoPlayHero();
  });

  sliderElement.addEventListener('mouseleave', () => {
    isDragging = false;
    startAutoPlayHero();
  });

  // Keyboard events
  document.addEventListener('keydown', (e) => {
    const activePageIsHome = document.getElementById('home').classList.contains('active');
    if (!activePageIsHome) return;

    if (e.key === 'ArrowLeft') slideHero(-1);
    if (e.key === 'ArrowRight') slideHero(1);
  });

  startAutoPlayHero();
}

function handleHeroSwipe() {
  const swipeThreshold = 50;
  const diff = heroSliderTouchStart - heroSliderTouchEnd;

  if (Math.abs(diff) > swipeThreshold) {
    if (diff > 0) {
      slideHero(1); // Swipe left → next
    } else {
      slideHero(-1); // Swipe right → prev
    }
  }
}

function slideHero(direction) {
  currentHeroSlide += direction;
  
  if (currentHeroSlide >= totalHeroSlides) {
    currentHeroSlide = 0;
  } else if (currentHeroSlide < 0) {
    currentHeroSlide = totalHeroSlides - 1;
  }

  updateHeroSlider();
  clearInterval(heroAutoPlayInterval);
  startAutoPlayHero();
}

function slideToHero(index) {
  currentHeroSlide = index;
  updateHeroSlider();
  clearInterval(heroAutoPlayInterval);
  startAutoPlayHero();
}

function updateHeroSlider() {
  const slides = document.querySelectorAll('.hero-slide');
  const indicators = document.querySelectorAll('.indicator');

  slides.forEach((slide, index) => {
    slide.classList.remove('active');
    if (index === currentHeroSlide) {
      slide.classList.add('active');
    }
  });

  indicators.forEach((indicator, index) => {
    indicator.classList.remove('active');
    if (index === currentHeroSlide) {
      indicator.classList.add('active');
    }
  });

  document.getElementById('heroSlideCount').textContent = currentHeroSlide + 1;
}

function startAutoPlayHero() {
  heroAutoPlayInterval = setInterval(() => {
    slideHero(1);
  }, 6000);
}

