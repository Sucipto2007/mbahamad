function navigateToEvent(eventId) {
  currentEventId = eventId;
  const event = eventsData.find(e => e.id === eventId);
  
  if (!event) return;

  // Update content
  document.getElementById('event-num').textContent = event.number;
  document.getElementById('event-title').textContent = event.title;
  document.getElementById('event-date').textContent = event.date;
  document.getElementById('breadcrumb-title').textContent = event.title;
  
  // Format content dengan styling yang lebih baik
  const formattedContent = event.content;
  document.getElementById('event-content').innerHTML = formattedContent;
  document.getElementById('event-quote').textContent = event.quote;

  // Update video section - PENTING
  const videoSection = document.getElementById('video-section');
  const videoPlayer = document.getElementById('video-player');
  const videoSource = document.getElementById('video-source');
  
  if (event.videoUrl && event.videoUrl.trim() !== '') {
    videoSection.style.display = 'block';
    videoSource.src = event.videoUrl;
    
    // Reset video player
    videoPlayer.load();
    
    document.getElementById('video-caption').textContent = event.videoCaption;
    
    // Debug: Log video URL
    console.log('Video URL:', event.videoUrl);
  } else {
    videoSection.style.display = 'none';
  }

  // Update button states
  updateNavButtons();

  // Navigate to event detail page
  navigateToPage('event-detail');
  
  // Add smooth scroll to content
  setTimeout(() => {
    document.getElementById('event-content').scrollIntoView({ behavior: 'smooth', block: 'start' });
  }, 300);
}

